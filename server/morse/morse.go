const (
	Short      = '.'
	Long       = '-'
	ShortPause = ' '
	LongPause  = '/'
)

func Marshal(text string) string {
	sb := strings.Builder{}
	for _, char := range text {
		morseChar, ok := morse[string(char)]
		if !ok {
			continue
		}
		sb.WriteString(morseChar)
	}
	return sb.String()
}
